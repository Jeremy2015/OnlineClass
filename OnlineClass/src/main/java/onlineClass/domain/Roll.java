package onlineClass.domain;

public class Roll {

	private String course_id;
	private String roll_picture;
	
	
	public String getCourse_id() {
		return course_id;
	}
	public void setCourse_id(String class_id) {
		this.course_id = class_id;
	}
	public String getRoll_picture() {
		return roll_picture;
	}
	public void setRoll_picture(String roll_picture) {
		this.roll_picture = roll_picture;
	}
	
}
